first_num = int(input("Give me first number: "))
second_num = int(input("Give me second number: "))
third_num = int(input("Give me third number: "))

summma = first_num + second_num + third_num
tulo = first_num * second_num * third_num
keski_arvo = (first_num + second_num + third_num) / 2

